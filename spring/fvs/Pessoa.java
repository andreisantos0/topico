package fvs;

public class Pessoa {

	private Interger id;
	private String nome;
	private String cpf;
	public Interger getId() {
		return id;
	}
	public void setId(Interger id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	}


